#include <unistd.h>

int	main(int ac, char *av[])
{
	int		t;
	int		j;
	char	*str;

	if (ac == 2)
	{
		t = 1;
		j = 0;
		str = av[1];
		while (*str != '\0')
		{
			if (*str == ' ' || *str == '\t')
				t = 0;
			else
			{
				if (t == 0 && j == 1)
					write(1, " ", 1);
				t = 1;
				j = 1;
				write(1, str, 1);
			}
			str++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
